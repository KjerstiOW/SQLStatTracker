class MapData:
    def __init__(self, date, game_type, friendly_team, enemy_team, map_name, friendly_score, enemy_score, result, vod_code):
        self.date = date
        self.game_type = game_type
        self.friendly_team = friendly_team
        self.enemy_team = enemy_team
        self.map_name = map_name
        self.friendly_score = friendly_score
        self.enemy_score = enemy_score
        self.result = result
        self.vod_code = vod_code